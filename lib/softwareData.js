// Group software by category
export const softwareByCategory = softwareData.reduce((acc, software) => {
    if (!acc[software.category]) {
        acc[software.category] = [];
    }
    acc[software.category].push(software);
    return acc;
}, {});

// Get software by ID
export function getSoftwareById(id) {
    return softwareData.find(software => software.id === id);
}

